zp = int(input('Введите заработную плату в месяц: '))
proc_ipo = int(input('Введите, какой процент(%) уходит на ипотеку: '))
proc_life = int(input('Введите, какой процент(%) уходит на жизнь: '))
print('На ипотеку было потрачено:', round(zp*proc_ipo/100*12), 'рублей')
print('Было накоплено:', round(zp*(100-proc_ipo-proc_life)/100*12), 'рублей')
